                    <div class="headFour">MIND Diet</div>

                    <div class="textFourContainer">

                        Research has found that the MIND (Mediterranean-DASH Intervention for Neurodegenerative Delay) diet substantially slows cognitive 
                        decline with age. Further, studies have also found that the MIND diet is associated with better cognitive functioning independent 
                        of common brain pathology, suggesting it may contribute to cognitive resilience in the elderly.

                        <br>“This diet is a combination of the Mediterranean diet and the DASH diet, so it’s both heart-healthy and, again, the moderate 
                        carbs could give it a little edge over the DASH diet for diabetes,” says Dr. Jampolis. “It may be a little more challenging to hit 
                        the specific weekly food targets, but if you do, your brain will surely benefit.”<br>The MIND diet combines certain hallmarks of the 
                        Mediterranean and DASH diets, emphasizing a plant-based plate and limited intake of animal products and highly-saturated fats.

                    </div>

                </div>

