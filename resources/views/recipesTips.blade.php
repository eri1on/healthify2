                    <div class="headTwo">Dash Diet</div>

                    <div class="textTwoContainer">

                        The DASH (Dietary Approaches to Stop Hypertension) diet was designed specifically with heart health in mind.
                        It takes a low-sodium approach to eating, and studies show the DASH diet effectively lowers blood pressure and
                        “bad” LDL cholesterol. “You may find it harder to eat out with the reduced sodium … and cooking without adding
                        salt may be more challenging as well, but adding in spices and herbs can make this diet even healthier,” says Melina Jampolis, 
                        M.D, a board-certified physician nutritionist, Forbes Health Advisory Board member and panelist for our best diets ranking. The DASH diet 
                        recommends meals rich in fruits, vegetables and low-fat dairy and limited in saturated fat. On the DASH diet, you use a chart to estimate 
                        your daily calorie needs, which are then used to figure out how many servings from each food group you should have.

                    </div>

                </div>

            </div>



