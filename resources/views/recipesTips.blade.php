                    <div class="headThree">Flexitarian Diet</div>

                    <div class="textThreeContainer">

                        For those who want to reap the benefits of a plant-based eating plan, but don’t feel like they can or want to commit 
                        to giving up that slab of bacon at brunch quite yet, the flexitarian diet might be a good fit. Described by the International 
                        Food Information Council as a “semi-vegetarian, plant-forward diet,” the flexitarian diet emphasizes plant-based foods (like fruits, 
                        vegetables and legumes) dairy and eggs, and recommends less frequent and/or smaller portions of meat. The organization notes that 
                        emerging research suggests this way of eating may have benefits ranging from weight management to reduced risk of cardiovascular 
                        disease and diabetes. This eating plan encourages followers to think about what can be added to their plate, as opposed to what 
                        should be taken away, according to the International Food Information Council.
                    </div>

