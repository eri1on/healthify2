                    <div class="headSeven">Pescatarian Diet</div>

                    <div class="textSevenContainer">

                        Another iteration of vegetarianism, the pescatarian diet—which is mainly plant-based with the inclusion of fish and other seafood—earned
                        high marks for categories like ease of use and safety. A 2020 study found that pescatarians are less likely to be living with obesity and 
                        have a lower prevalence of cardiovascular risk factors such as high BMI, hypertension and hypercholesterolaemia.
                        <br>“Studies consistently show that vegetarian diets are associated with a lower risk of diabetes, heart disease and less weight gain/lower 
                        weight than diets that include animal protein/meat, however they can be restrictive, so a pescatarian diet that includes fish is less restrictive 
                        and can make a predominantly plant-based diet more livable,” says Dr. Jampolis.
                        Followers of this diet are less likely to struggle with obesity or hypertension.
                        Risk of consuming too much mercury or toxins if seafood intake isn’t varied.
                        Associated with lower risk of diabetes and heart disease

